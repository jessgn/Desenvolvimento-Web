//Exercício 1- Triângulo

/*  Escreva um loop que realiza 7 chamadas do comando console.log e exiba um "triângulo" */

var n = 7;
var simb, j;

for (var i = 0; i < n; i++){
  simb = i + 1;
  j = '#'.repeat(simb);
  console.log(j);    
}  


